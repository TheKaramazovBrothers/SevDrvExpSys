#define		TRUE		1
#endif
//------------------------------------------------------------------------------------------------
#ifndef		BUSY
#define		BUSY		1
#endif
//------------------------------------------------------------------------------------------------
#ifndef		FREE		
#define		FREE		0
#endif
//------------------------------------------------------------------------------------------------
#ifndef		WAIT		
#define		WAIT		1
#endif
//------------------------------------------------------------------------------------------------
#ifndef		NOWAIT
#define		NOWAIT		0
#endif
//------------------------------------------------------------------------------------------------
#ifndef		ENABLE
#define		ENABLE		1
#endif
//------------------------------------------------------------------------------------------------
#ifndef		DISABLE		
#define		DISABLE		0
#endif
//------------------------------------------------------------------------------------------------
#ifndef     NULL
#define     NULL        0
#endif


#define		IQAbsCal(x)					(((x)>=0) ? (x) : -(x))
//------------------------------------------------------------------------------------------------
#define		IQMAX(a,b)					(((a) > (b)) ? (a) : (b))
#define		IQMIN(a,b)					(((a) < (b)) ? (a) : (b))
#define		IQMaxOfThree(A,B,C)			(((A)>(B))?(((A)>(C))?(A):(C)):(((B)>(C))?(B):(C)))
#define		IQMinOfThree(A,B,C)			(((A)<(B))?(((A)<(C))?(A):(C)):(((B)<(C))?(B):(C)))
//------------------------------------------------------------------------------------------------
#define		IQSatLimit(A,Ulim,Llim)		(((A)>(Ulim))?(Ulim):(((A)<(Llim))?(Llim):(A)))



#endif

